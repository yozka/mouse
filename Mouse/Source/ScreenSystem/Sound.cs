using System;
using Microsoft.Xna.Framework.Audio;

namespace Mouse.ScreenSystem
{
    ///--------------------------------------------------------------------------------------






     ///=====================================================================================
    ///
    /// <summary>
    /// Система звуковых эффектов
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class ASound
    {
        ///--------------------------------------------------------------------------------------
        private bool m_sound = false;
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Включенна выключена звуковые эффекты
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public bool enabled
        {
            get
            {
                return m_sound;
            }
            set
            {
                m_sound = value;
            }
        }
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// проигрование звуков
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void play(SoundEffect sound)
        {
            if (m_sound && sound != null)
            {
                sound.Play();
            }
        }
        ///--------------------------------------------------------------------------------------


    }
}
