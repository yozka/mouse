using System;
using Microsoft.Xna.Framework;


namespace Mouse.GameProgramms.GameObjects.Units
{
    ///--------------------------------------------------------------------------------------
    

    



     ///=====================================================================================
    ///
    /// <summary>
    /// Базовый класс интелекта для объектов
    /// </summary>
    /// 
    ///--------------------------------------------------------------------------------------
    public class AActionAI
    {
        
        
        ///--------------------------------------------------------------------------------------
        private AActionAI m_nextAI = null; //следующий по списку привязанный интелект
        ///--------------------------------------------------------------------------------------





        

         ///=====================================================================================
        ///
        /// <summary>
        /// Конструктор 1
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public AActionAI()
        {
        }
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// Конструктор 2
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public AActionAI(AActionAI ai)
        {
            m_nextAI = ai;
        }
        ///--------------------------------------------------------------------------------------







        

         ///=====================================================================================
        ///
        /// <summary>
        /// Обновление внутренней логики объекта
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void update(TimeSpan gameTime, AUnitObject unit)
        {
            onUpdate(gameTime, unit);
            if (m_nextAI != null)
            {
                m_nextAI.update(gameTime, unit);
            }
        }
        ///--------------------------------------------------------------------------------------






         ///=====================================================================================
        ///
        /// <summary>
        /// Обновление внутренней логики объекта
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        protected virtual void onUpdate(TimeSpan gameTime, AUnitObject unit)
        {
        }
        ///--------------------------------------------------------------------------------------








         ///=====================================================================================
        ///
        /// <summary>
        /// Обработка столкновения с препятствием
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        public void blockageMove(Vector2 newPosition, AUnitObject unit)
        {
            onBlockageMove(newPosition, unit);
            if (m_nextAI != null)
            {
                m_nextAI.blockageMove(newPosition, unit);
            }
        }
        ///--------------------------------------------------------------------------------------







         ///=====================================================================================
        ///
        /// <summary>
        /// Обработка столкновения с препятствием
        /// </summary>
        /// 
        ///--------------------------------------------------------------------------------------
        protected virtual void onBlockageMove(Vector2 newPosition, AUnitObject unit)
        {
        }
        ///--------------------------------------------------------------------------------------



    }
}
